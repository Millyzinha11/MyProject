# Demonstração do uso de operadores
# Criar um programa que faça o somatório de 3 valores e tire sua média...

X = int (input("Digite o valor 1:"))
Y = int (input("Digite o valor 2:"))
Z = int (input("Digite o valor 3:"))
soma = X + Y + Z
media = soma / 3

print("A soma é:", soma)
print(f"A média é:, {media}")
