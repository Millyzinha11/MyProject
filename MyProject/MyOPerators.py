# Demonstração de expressões complexas

# Declaração de variáveis que iremos usar...
X = 10; Y = 20; Z = 30

valor = X * (Y / 10 + Z / 10) - 25
print("O resultado da expressão #1 e:", valor)

valor = ((X - Y) + 10 + (Z - X ) ) / 3
print("O resultado da expressão #2 e:", valor)

valor = (X + (Y * 2 + (Z / 10 ))) - 18
print("O resultado da expressão #3 e:", valor)

